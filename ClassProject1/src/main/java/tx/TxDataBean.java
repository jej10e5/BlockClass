package tx;

public class TxDataBean {
	private int tx_no;
	private String tx_id;
	private String blc_id;
	private String buyer;
	private String seller;
	private String item;
	private int price;
	private String txtime;
	
	public int getTx_no() {
		return tx_no;
	}
	public void setTx_no(int tx_no) {
		this.tx_no = tx_no;
	}
	public String getTx_id() {
		return tx_id;
	}
	public void setTx_id(String tx_id) {
		this.tx_id = tx_id;
	}
	public String getBlc_id() {
		return blc_id;
	}
	public void setBlc_id(String blc_id) {
		this.blc_id = blc_id;
	}
	public String getBuyer() {
		return buyer;
	}
	public void setBuyer(String buyer) {
		this.buyer = buyer;
	}
	public String getSeller() {
		return seller;
	}
	public void setSeller(String seller) {
		this.seller = seller;
	}
	public String getItem() {
		return item;
	}
	public void setItem(String item) {
		this.item = item;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getTxtime() {
		return txtime;
	}
	public void setTxtime(String txtime) {
		this.txtime = txtime;
	}
	

	
	
}


